"""
Los booleanos son un tipo de dato que solo puede tener dos valores posibles: True (verdadero) o False (falso). Se utilizan comúnmente en lógica y condiciones para controlar el flujo de un programa.
"""

# Ejemplos de booleanos
es_mayor = True
tiene_licencia = False
come_frutas = True

print("¿Es mayor de edad?: ", es_mayor)
print("¿Tiene licencia de conducir?: ", tiene_licencia)
print("¿Come frutas regularmente?: ", come_frutas)